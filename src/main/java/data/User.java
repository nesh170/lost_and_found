package data;

public class User {
    public String name;
    public String uniqueId;
    public String netId;
    public String email;

    public User(String name, String uniqueId, String netId, String email){
        this.name = name;
        this.uniqueId = uniqueId;
        this.netId = netId;
        this.email = email;
    }

}
